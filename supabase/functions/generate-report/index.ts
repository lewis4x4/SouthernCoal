// ── Auth ─────────────────────────────────────────────────────────────────────
async function authenticate(req: Request, sb: SB): Promise<Auth | null> {
  const hdr = req.headers.get("Authorization");
  if (!hdr?.startsWith("Bearer ")) return null;
  const {
    data: { user },
    error,
  } = await sb.auth.getUser(hdr.replace("Bearer ", ""));
  if (error || !user) return null;

  const { data: profile } = await sb
    .from("user_profiles")
    .select("id, organization_id")
    .eq("id", user.id)
    .single();
  if (!profile) return null;

  return {
    userId: user.id,
    orgId: profile.organization_id as string,
  };
}

// ── Helpers ──────────────────────────────────────────────────────────────────
async function getVisibleOrgIds(sb: SB, userOrgId: string): Promise<string[]> {
  const { data: orgs } = await sb
    .from("organizations")
    .select("id, parent_id")
    .eq("is_active", true);
  if (!orgs) return [userOrgId];

  const userOrg = orgs.find((o: Record<string, unknown>) => o.id === userOrgId);
  if (!userOrg) return [userOrgId];

  const parentId = userOrg.parent_id as string | null;
  return orgs
    .filter(
      (o: Record<string, unknown>) =>
        o.id === userOrgId ||
        o.parent_id === userOrgId ||
        (parentId && (o.id === parentId || o.parent_id === parentId)),
    )
    .map((o: Record<string, unknown>) => o.id as string);
}

async function getSiteIds(sb: SB, orgIds: string[]): Promise<string[]> {
  const { data } = await sb
    .from("sites")
    .select("id")
    .in("organization_id", orgIds);
  return (data ?? []).map((s: Record<string, unknown>) => s.id as string);
}

function ninetyDaysAgo(): string {
  const d = new Date();
  d.setDate(d.getDate() - 90);
  return d.toISOString().split("T")[0];
}

const CSV_INJECTION_RE = /^[=+\-@\t\r]/;

function escapeCsv(v: unknown): string {
  if (v === null || v === undefined) return "";
  let s = String(v);
  // Neutralize CSV formula injection (=, +, -, @, tab, cr)
  if (CSV_INJECTION_RE.test(s)) {
    s = `'${s}`;
  }
  return s.includes(",") || s.includes('"') || s.includes("\n") || s.includes("'")
    ? `"${s.replace(/"/g, '""')}"`
    : s;
}

function toCsv(columns: string[], rows: unknown[][]): string {
  const header = columns.map(escapeCsv).join(",");
  const body = rows.map((r) => r.map(escapeCsv).join(",")).join("\n");
  return `${header}\n${body}\n\n"${DISCLAIMER}"`;
}

function makeFlags(rows: unknown[][]): Record<string, unknown> {
  const flags: Record<string, unknown> = {};
  if (rows.length >= MAX_ROWS) flags.row_limit_hit = true;
  return flags;
}

// ── Input Validation ────────────────────────────────────────────────────────
const VALID_STATES = new Set(["AL", "KY", "TN", "VA", "WV"]);
const UUID_RE = /^[0-9a-f]{8}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{12}$/i;
const ISO_DATE_RE = /^\d{4}-\d{2}-\d{2}$/;
const ALLOWED_FORMATS = new Set(["csv"]);

function sanitizeConfig(cfg: Config): Config {
  const clean: Config = {};
  if (cfg.date_from && typeof cfg.date_from === "string" && ISO_DATE_RE.test(cfg.date_from) && !isNaN(Date.parse(cfg.date_from))) {
    clean.date_from = cfg.date_from;
  }
  if (cfg.date_to && typeof cfg.date_to === "string" && ISO_DATE_RE.test(cfg.date_to) && !isNaN(Date.parse(cfg.date_to))) {
    clean.date_to = cfg.date_to;
  }
  if (Array.isArray(cfg.states)) {
    clean.states = cfg.states
      .filter((s): s is string => typeof s === "string" && VALID_STATES.has(s.toUpperCase()))
      .map((s) => s.toUpperCase());
  }
  if (Array.isArray(cfg.org_ids)) {
    clean.org_ids = cfg.org_ids.filter(
      (id): id is string => typeof id === "string" && UUID_RE.test(id),
    );
  }
  return clean;
}

// ── Report #1: Permit Inventory ──────────────────────────────────────────────
async function rptPermitInventory(
  sb: SB,
  orgIds: string[],
  _cfg: Config,
): Promise<Result> {
  const { data, error } = await sb
    .from("npdes_permits")
    .select(
      `permit_number, permit_type, status, issued_date, effective_date,
       expiration_date, issuing_agency, facility_name, permittee_name,
       sites(name, city, county, states(code)),
       organizations(name)`,
    )
    .in("organization_id", orgIds)
    .order("permit_number")
    .limit(MAX_ROWS);
  if (error) throw new Error(error.message);

  const columns = [
    "Permit Number", "Type", "Status", "Issued", "Effective", "Expiration",
    "Agency", "Facility", "Permittee", "Site", "City", "County", "State",
    "Organization",
  ];
  const rows = (data ?? []).map((r: Record<string, unknown>) => {
    const site = r.sites as Record<string, unknown> | null;
    const state = site?.states as Record<string, unknown> | null;
    const org = r.organizations as Record<string, unknown> | null;
    return [
      r.permit_number, r.permit_type, r.status, r.issued_date,
      r.effective_date, r.expiration_date, r.issuing_agency, r.facility_name,
      r.permittee_name, site?.name, site?.city, site?.county, state?.code,
      org?.name,
    ];
  });
  return { columns, rows, flags: makeFlags(rows) };
}

// ── Report #2: Permit Renewal Tracker ────────────────────────────────────────
async function rptPermitRenewalTracker(
  sb: SB,
  orgIds: string[],
  _cfg: Config,
): Promise<Result> {
  const { data, error } = await sb
    .from("npdes_permits")
    .select(
      `permit_number, status, effective_date, expiration_date,
       administratively_continued, facility_name,
       sites(name, states(code)), organizations(name)`,
    )
    .in("organization_id", orgIds)
    .order("expiration_date")
    .limit(MAX_ROWS);
  if (error) throw new Error(error.message);

  const today = new Date();
  const columns = [
    "Permit Number", "Status", "Effective", "Expiration",
    "Days Until Expiration", "Admin Continued", "Facility", "Site", "State",
    "Organization",
  ];
  const rows = (data ?? []).map((r: Record<string, unknown>) => {
    const site = r.sites as Record<string, unknown> | null;
    const state = site?.states as Record<string, unknown> | null;
    const org = r.organizations as Record<string, unknown> | null;
    const exp = r.expiration_date ? new Date(r.expiration_date as string) : null;
    const daysUntil = exp
      ? Math.round((exp.getTime() - today.getTime()) / 86_400_000)
      : null;
    return [
      r.permit_number, r.status, r.effective_date, r.expiration_date,
      daysUntil, r.administratively_continued ? "Yes" : "No", r.facility_name,
      site?.name, state?.code, org?.name,
    ];
  });
  return { columns, rows, flags: makeFlags(rows) };
}

