// ── Report #4: Permit Limit Parameter Matrix ─────────────────────────────────
async function rptPermitLimitMatrix(
  sb: SB,
  orgIds: string[],
  _cfg: Config,
): Promise<Result> {
  // 2-query approach: get permit IDs for visible orgs, then limits
  const { data: permits } = await sb
    .from("npdes_permits")
    .select("id, permit_number")
    .in("organization_id", orgIds);
  const permitMap = new Map(
    (permits ?? []).map((p: Record<string, unknown>) => [p.id, p.permit_number]),
  );
  const permitIds = [...permitMap.keys()] as string[];
  if (!permitIds.length) return { columns: [], rows: [], flags: {} };

  const { data, error } = await sb
    .from("permit_limits")
    .select(
      `permit_id, storet_code, limit_type, limit_value, unit,
       statistical_base, monitoring_frequency, sample_type,
       effective_date, end_date, review_status,
       outfalls(outfall_number)`,
    )
    .in("permit_id", permitIds)
    .order("storet_code")
    .limit(MAX_ROWS);
  if (error) throw new Error(error.message);

  const columns = [
    "Permit Number", "Outfall", "STORET Code", "Limit Type", "Limit Value",
    "Unit", "Statistical Base", "Monitoring Frequency", "Sample Type",
    "Effective", "End Date", "Review Status",
  ];
  const rows = (data ?? []).map((r: Record<string, unknown>) => {
    const outfall = r.outfalls as Record<string, unknown> | null;
    return [
      permitMap.get(r.permit_id as string) ?? "", outfall?.outfall_number,
      r.storet_code, r.limit_type, r.limit_value, r.unit, r.statistical_base,
      r.monitoring_frequency, r.sample_type, r.effective_date, r.end_date,
      r.review_status,
    ];
  });
  return { columns, rows, flags: makeFlags(rows) };
}

