// ── Main Handler ─────────────────────────────────────────────────────────────
Deno.serve(async (req: Request) => {
  if (req.method === "OPTIONS") {
    return new Response("ok", { headers: corsHeaders });
  }
  if (req.method !== "POST") {
    return json({ error: "Method not allowed" }, 405);
  }

  const sb = createClient(SUPABASE_URL, SERVICE_KEY);

  // 1. Authenticate
  const auth = await authenticate(req, sb);
  if (!auth) return json({ error: "Unauthorized" }, 401);

  // 2. Parse body
  let body: Record<string, unknown>;
  try {
    body = await req.json();
  } catch {
    return json({ error: "Invalid JSON body" }, 400);
  }

  const reportKey = body.report_key as string;
  const format = ALLOWED_FORMATS.has(body.format as string) ? (body.format as string) : "csv";
  const config = sanitizeConfig((body.config as Config) ?? {});

  if (!reportKey || typeof reportKey !== "string") {
    return json({ error: "report_key required" }, 400);
  }

  // 3. Validate report definition
  const { data: reportDef } = await sb
    .from("report_definitions")
    .select("id, report_key, report_number, title, is_locked")
    .eq("report_key", reportKey)
    .single();

  if (!reportDef) return json({ error: "Unknown report key" }, 400);
  if (reportDef.is_locked) {
    return json(
      { error: "Report unavailable — prerequisite data not yet uploaded" },
      400,
    );
  }

  // 4. Check permission
  const { data: roleAssignments } = await sb
    .from("user_role_assignments")
    .select("roles(name)")
    .eq("user_id", auth.userId);

  const userRoles = (roleAssignments ?? [])
    .map((r: Record<string, unknown>) => {
      const role = r.roles as Record<string, unknown> | null;
      return role?.name as string | undefined;
    })
    .filter(Boolean) as string[];

  if (userRoles.length > 0) {
    const { data: perms } = await sb
      .from("report_role_permissions")
      .select("role_name")
      .eq("report_definition_id", reportDef.id)
      .in("role_name", userRoles);

    if (!perms || perms.length === 0) {
      return json({ error: "No permission for this report" }, 403);
    }
  } else {
    return json({ error: "No permission for this report" }, 403);
  }

  // 5. Check registry
  const reportFn = REGISTRY[reportKey];
  if (!reportFn) {
    return json({ error: "Report not implemented" }, 400);
  }

  // 6. Create job row
  const { data: job, error: insertError } = await sb
    .from("generated_reports")
    .insert({
      organization_id: auth.orgId,
      report_definition_id: reportDef.id,
      created_by: auth.userId,
      report_config: config,
      status: "generating",
      format,
    })
    .select("id")
    .single();

  if (insertError || !job) {
    console.error("[generate-report] Insert error:", insertError?.message);
    return json({ error: "Failed to create report job" }, 500);
  }

  const jobId = job.id as string;

  try {
    // 7. Get visible org IDs (apply config.org_ids filter if present)
    let orgIds = await getVisibleOrgIds(sb, auth.orgId);
    if (config.org_ids?.length) {
      const allowed = new Set(orgIds);
      orgIds = config.org_ids.filter((id) => allowed.has(id));
    }

    // 8. Run report
    const result = await reportFn(sb, orgIds, config);

    // 9. Generate CSV
    const csv = toCsv(result.columns, result.rows);
    const csvBytes = new TextEncoder().encode(csv);

    // 10. Upload to storage
    const dateStr = new Date().toISOString().split("T")[0];
    const num = String(reportDef.report_number).padStart(3, "0");
    const fileName = `RPT-${num}-${dateStr}.csv`;
    const storagePath = `${auth.orgId}/${reportKey}/${fileName}`;

    const { error: uploadError } = await sb.storage
      .from("generated-reports")
      .upload(storagePath, csvBytes, {
        contentType: "text/csv; charset=utf-8",
        upsert: true,
      });
    if (uploadError) throw new Error(`Upload failed: ${uploadError.message}`);

    // 11. Update job as complete
    await sb
      .from("generated_reports")
      .update({
        status: "complete",
        file_path_csv: storagePath,
        file_size_bytes: csvBytes.length,
        row_count: result.rows.length,
        data_quality_flags: result.flags,
        completed_at: new Date().toISOString(),
      })
      .eq("id", jobId);

    // 12. Audit log (fire-and-forget)
    sb.from("audit_log")
      .insert({
        user_id: auth.userId,
        organization_id: auth.orgId,
        action: "report_generated",
        module: "reports",
        table_name: "generated_reports",
        record_id: jobId,
        description: JSON.stringify({
          report_key: reportKey,
          format,
          row_count: result.rows.length,
        }),
        created_at: new Date().toISOString(),
      })
      .then(({ error: auditErr }) => {
        if (auditErr) console.warn("[generate-report] Audit log failed:", auditErr.message);
      })
      .catch((err: unknown) => {
        console.warn("[generate-report] Audit log exception:", err);
      });

    return json({ success: true, job_id: jobId });
  } catch (err) {
    const rawMsg = err instanceof Error ? err.message : "Report generation failed";
    // Truncate to prevent schema info leakage in stored error
    const message = rawMsg.length > 500 ? rawMsg.slice(0, 500) : rawMsg;
    console.error("[generate-report] Error:", message);

    // Update job as failed
    await sb
      .from("generated_reports")
      .update({
        status: "failed",
        error_message: message,
        completed_at: new Date().toISOString(),
      })
      .eq("id", jobId);

    // Return job_id so frontend can poll and see the failure state
    return json({ success: false, job_id: jobId, error: "Report generation failed" });
  }
});
